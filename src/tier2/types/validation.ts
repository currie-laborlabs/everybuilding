// ─── Retry context ────────────────────────────────────────────────────────────

/**
 * Passed back to the composer when a retry is needed.
 * The composer appends the corrective_instruction to the next user prompt.
 */
export interface ValidationRetryContext {
  attempt_number: 2; // only ever 2 — one retry max
  original_violations: EmailValidationViolation[];
  corrective_instruction: string;
}
