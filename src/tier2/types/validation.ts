/**
 * Email validation types — EveryBuilding Tier 2
 *
 * Every generated email is validated before being pushed to Instantly.
 * A failed validation triggers one corrective retry with an amended prompt.
 * If the retry also fails, the email is logged and skipped for this cycle.
 *
 * Validation checks:
 *   - Hard violations (must retry): word count, missing building reference, CTA mismatch
 *   - Soft violations (warn only): tone flags, phrase violations
 */

import type { CtaNumber, CtaName } from "./cta";

// ─── The generated email structure ───────────────────────────────────────────

/**
 * The parsed output from Claude Haiku before validation.
 * The LLM is expected to return a structured JSON object matching this shape.
 */
export interface GeneratedEmail {
  subject: string;
  /** Full email body text (excludes subject, signature line, sender sign-off) */
  body: string;
  /**
   * The Company Signature Line — one sentence between body and sender name.
   * Generated by the LLM using the resolved SignatureLineData.
   */
  signature_line: string;
  /** Sender sign-off first name (from SenderIdentity.sender_sign_off_name) */
  sender_sign_off: string;
  /** Full sender name (from SenderIdentity.sender_full_name) */
  sender_full_name: string;
  /** Sender title (from SenderIdentity.sender_title) */
  sender_title: string;
  /** Computed at validation time — word count of body only (excludes subject + signature) */
  word_count: number;
  /** CTA number the LLM reports it used (cross-checked against the requested cta_number) */
  cta_number_used: CtaNumber;
  cta_name_used: CtaName;
}

// ─── Validation rule ──────────────────────────────────────────────────────────

export type ValidationSeverity =
  | "hard"  // MUST retry — email cannot be sent with this violation
  | "soft"; // Log and warn — email can be sent but issue is flagged

export interface EmailValidationRule {
  rule_id: string;
  description: string;
  severity: ValidationSeverity;
}

// ─── Validation result ────────────────────────────────────────────────────────

export interface EmailValidationViolation {
  rule_id: string;
  description: string;
  severity: ValidationSeverity;
}

export interface EmailValidationResult {
  /** true only if zero hard violations (soft violations still pass) */
  passed: boolean;
  violations: EmailValidationViolation[];
  word_count: number;
  /**
   * If there are hard violations, this is a targeted corrective prompt
   * appended to the next LLM attempt explaining what needs to change.
   * null if passed (or soft violations only).
   */
  retry_prompt: string | null;
  validated_at: string; // ISO timestamp
}

